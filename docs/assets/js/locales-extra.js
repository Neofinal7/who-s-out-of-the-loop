 function installExtraLangs(){
  Object.assign(LANG_LABELS,{hi:'HI 🇮🇳',es:'ES 🇪🇸',pt:'PT 🇧🇷'});
 Object.entries(EXTRA_LANGS).forEach(([code,pack])=>{
   LANGS[code]={...LANGS.en,...pack,...(EXTRA_RUNTIME_STRINGS[code]||{}),dir:'ltr'};
  });
 }

 function installTopics(){
  Object.entries(EXTRA_TOPIC_PAIRS).forEach(([code,pairs])=>{
   pairs.forEach((pair,idx)=>{
    if(TOPICS[idx]) TOPICS[idx][code]=pair;
   });
  });
 }

 function installStories(){
  Object.entries(EXTRA_STORIES).forEach(([code,stories])=>{
   stories.forEach((entry,idx)=>{
    if(!STORIES[idx]) return;
    STORIES[idx].insider[code]=entry.insider;
    STORIES[idx].outsider[code]=entry.outsider;
   });
  });
 }

 function applyCulturalTweaks(){
  Object.entries(CULTURAL_TOPIC_OVERRIDES).forEach(([code,overrides])=>{
   Object.entries(overrides).forEach(([idx,pair])=>{
    const i=Number(idx);
    if(TOPICS[i]) TOPICS[i][code]=pair;
   });
  });
  Object.entries(CULTURAL_STORY_OVERRIDES).forEach(([code,overrides])=>{
   Object.entries(overrides).forEach(([idx,entry])=>{
    const i=Number(idx);
    if(!STORIES[i]) return;
    STORIES[i].insider[code]=entry.insider;
    STORIES[i].outsider[code]=entry.outsider;
   });
  });
 }

